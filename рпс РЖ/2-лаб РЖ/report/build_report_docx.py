# -*- coding: utf-8 -*-
"""Generate updated Word report for Lab 2."""
from pathlib import Path

from docx import Document
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.shared import Cm, Pt


def main():
    doc = Document()
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(14)
    for section in doc.sections:
        section.top_margin = Cm(2)
        section.bottom_margin = Cm(2)
        section.left_margin = Cm(3)
        section.right_margin = Cm(1.5)

    def p(text, bold=False):
        r = doc.add_paragraph()
        run = r.add_run(text)
        run.font.name = "Times New Roman"
        run.font.size = Pt(14)
        run.bold = bold
        r.paragraph_format.first_line_indent = Cm(1.25)
        r.paragraph_format.space_after = Pt(6)
        return r

    def heading(text, level=1):
        h = doc.add_heading(text, level=level)
        for run in h.runs:
            run.font.name = "Times New Roman"
        return h

    t = doc.add_paragraph()
    t.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r = t.add_run(
        "МИНОБРНАУКИ РОССИИ\n"
        "федеральное государственное бюджетное образовательное учреждение высшего образования\n"
        "«Санкт-Петербургский государственный технологический институт (технический университет)»"
    )
    r.font.name = "Times New Roman"
    r.font.size = Pt(12)
    t.paragraph_format.space_after = Pt(12)

    t2 = doc.add_paragraph()
    t2.alignment = WD_ALIGN_PARAGRAPH.CENTER
    r2 = t2.add_run(
        "Отчёт по лабораторной работе № 2\n"
        "Вариант № 22\n"
        "Тема: «Изучение реализации и расчёта сложности алгоритмов сортировки»"
    )
    r2.font.name = "Times New Roman"
    r2.font.size = Pt(14)
    r2.bold = True
    doc.add_paragraph()

    heading("СОДЕРЖАНИЕ", 1)
    for line in [
        "1. Цель работы",
        "2. Описание задания и реализация (вариант 22 — шейкерная сортировка)",
        "3. Структура проекта и функционал консольного приложения",
        "4. Блок-схема алгоритма (описание)",
        "5. Расчёт лучшей и худшей временной сложности",
        "6. Тестирование (unit-тесты)",
        "7. Выводы",
    ]:
        p(line)

    doc.add_page_break()
    heading("1. Цель работы", 1)
    p(
        "Разработать консольное приложение для сортировки целых чисел согласно варианту и оценить "
        "сложность реализованного алгоритма. В варианте 22 — шейкерная (коктейльная) сортировка."
    )

    heading("2. Описание задания и соответствие требованиям", 1)
    p("Согласно заданию лабораторной работы необходимо:")
    for it in [
        "описать алгоритм в виде блок-схемы (ниже — описание, которое используется при построении схемы);",
        "рассчитать лучшую и худшую временную сложность;",
        "оформить сортировку отдельным модулем без std::sort и аналогов;",
        "реализовать unit-тесты, запускаемые отдельно от приложения (проект rps2_tests);",
        "оформить отчёт.",
    ]:
        pr = doc.add_paragraph(it, style="List Bullet")
        for run in pr.runs:
            run.font.name = "Times New Roman"
            run.font.size = Pt(14)
    p(
        "Создание ветки в репозитории Git (из первой работы) и осмысленные сообщения коммитов — "
        "выполняются в системе контроля версий; в данном отчёте указывается как требование к работе."
    )

    heading("3. Структура проекта и функционал (папка rps2)", 1)
    p(
        "Проект решения: rps2.sln. Консольное приложение (rps2, main.cpp) реализует меню: "
        "1) ввод массива с клавиатуры; 2) генерация случайных целых чисел в диапазоне [1; 99]; "
        "3) загрузка чисел из файла. После формирования массива выводится исходный вид, "
        "выполняется shakerSort, выводится отсортированный массив. Пользователю предлагается "
        "сохранить в файл и исходный, и отсортированный массив (текстовый формат, подписи полей)."
    )
    p(
        "Модуль сортировки: файлы shaker_sort.h и shaker_sort.cpp, функция void shakerSort(std::vector<int>&). "
        "Сортировка на месте, используется std::swap, без сортировок стандартной библиотеки."
    )
    p(
        "Unit-тесты: отдельный проект rps2_tests, запуск через «Обозреватель тестов» в Visual Studio, "
        "отдельно от исполняемого файла rps2.exe."
    )

    heading("4. Блок-схема алгоритма (шейкерная сортировка)", 1)
    p(
        "Начало. Инициализация: left = 0, right = n−1, swapped = true. "
        "Условие внешнего цикла: (left < right) и swapped. "
        "Тело: swapped := false. Прямой проход: i от left до right−1; если a[i] > a[i+1], обмен и swapped := true. "
        "right := right − 1. Обратный проход: i от right до left+1; если a[i] < a[i−1], обмен и swapped := true. "
        "left := left + 1. Возврат к проверке внешнего условия. Конец."
    )

    heading("5. Расчёт лучшей и худшей временной сложности", 1)
    p("Пусть n — число элементов; оценка по n в нотации O(·).")
    p("Лучший случай: O(n).", bold=True)
    p(
        "Если массив уже отсортирован по возрастанию, при проходе не выполняется обмен, флаг swapped "
        "после итерации остаётся false, внешний цикл прекращается. Число сравнений пропорционально n, "
        "итого T_луч(n) = O(n)."
    )
    p("Худший случай: O(n²).", bold=True)
    p(
        "В неблагоприятном расположении (например, по убыванию) требуется много обменов. "
        "Суммарно порядок n² сравнений/обменов в квадратичной оценке; T_худ(n) = O(n²). "
        "Сумма (n−1) + (n−2) + … + 1 = n(n−1)/2 иллюстрирует квадратичный рост."
    )
    p("Ёмкостная сложность: O(1) — только несколько целочисленных переменных и тот же массив (in-place).")

    heading("6. Тестирование (unit-тесты)", 1)
    p(
        "В rps2_tests.cpp реализованы 10 тестов для shakerSort: обычный набор, уже отсортированный, "
        "обратный порядок, один элемент, пустой, отрицательные, все равны, два элемента, "
        "смешанные, большой массив (100 элементов). "
        "Результат: при запуске «Запуск всех тестов» в Обозревателе тестов — все 10 тестов пройдены."
    )

    heading("7. Выводы", 1)
    p(
        "Реализована шейкерная сортировка отдельным модулем; консольное приложение соответствует "
        "требуемому функционалу ввода, вывода и сохранения. Оценка сложности: лучший случай O(n), "
        "худший O(n²), память O(1). Корректность подтверждается набором unit-тестов. "
        "Для больших объёмов данных применяют алгоритмы с лучшей асимптотикой (например, O(n log n))."
    )

    doc.add_page_break()
    heading("Сведения об исполнителе (по исходному отчёту)", 1)
    table = doc.add_table(rows=6, cols=2)
    data = [
        ("Исполнитель", "Рузматов Ж.Т."),
        ("Курс, группа", "II, 445"),
        ("Проверили", "Дамрин А.О."),
        ("Дисциплина", "Разработка программных систем"),
        ("Проект", "rps2 (Visual Studio)"),
        ("Вариант", "22 — шейкерная сортировка"),
    ]
    for i, (a, b) in enumerate(data):
        table.rows[i].cells[0].text = a
        table.rows[i].cells[1].text = b

    out = Path(__file__).resolve().parent / "Отчет_ЛР2_РПС_Рузматов_Ж_обновлено.docx"
    doc.save(str(out))
    print("Saved:", out)


if __name__ == "__main__":
    main()
